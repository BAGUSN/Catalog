package com.snipl.ice.admin;

public class AdminBean {
	private String id="";
	private String name="";
	private String no_users="";
	private String description="";
	private String fst_Name="";
	private String lst_Name="";
	private String email="";
	private String mobile="";
	
	public void setId(String id) {
		this.id=id;
	}
	public String getId(){
		return id;
		
	}
	
	public void setName(String name) {
		this.name=name;
	}
	public String getName(){
		return name;
		
	}
	
	public void setNo_users(String no_users) {
		this.no_users=no_users;
	}
	public String getNo_users(){
		return no_users;
		
	}
	
	public void setDescription(String description) {
		this.description=description;
	}
	public String getDescription(){
		return description;
		
	}
	
	public void setFst_Name(String fst_Name) {
		this.fst_Name=fst_Name;
	}
	public String getFst_Name(){
		return fst_Name;
		
	}
	
	public void setLst_Name(String lst_Name) {
		this.lst_Name=lst_Name;
	}
	public String getLst_Name(){
		return lst_Name;
		
	}
	
	public void setEmail(String email) {
		this.email=email;
	}
	public String getEmail(){
		return email;
		
	}
	
	public void setMobile(String mobile) {
		this.mobile=mobile;
	}
	public String getMobile(){
		return mobile;
		
	}
}
